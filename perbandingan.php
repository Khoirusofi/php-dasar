<?php

$a = 10;
$b = 5;
$c = 10;

$a == $b; // sama dengan
var_dump($a == $b);

$a === $b; // sama dengan dan tipe data
var_dump($a === $b);

$a === $c; // sama dengan dan tipe data
var_dump($a === $c);

$a != $b; // tidak sama dengan
var_dump($a != $b);

$a !== $c; // tidak sama dengan
var_dump($a != $c);


// perbangdingan adalah operasi yang digunakan untuk membandingkan dua nilai
// operasi perbandingan yang dapat dilakukan pada php adalah sama dengan, tidak sama dengan, lebih besar, lebih kecil, lebih besar sama dengan, lebih kecil sama dengan