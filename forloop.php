<?php

// state - condition - increment
for ($a = 1; $a <= 10; $a++) {
  echo "Ini $a \n";
}

$b = -10;

// state - condition - decrement
for (; $b <= 10;) {
  echo "Ini $b \n";
  $b++;
}

for ($c = 2; $c <= 3; $c++) {
  echo "Ini $c \n";
}


//forloop digunakan untuk perulangan yang sudah diketahui batasnya
//forloop terdiri dari 3 bagian
// state - condition - action
