<?php

echo "Desimal : ";
var_dump(1234); // Desimal

echo "Oktal : ";
var_dump(0123); // Oktal

echo "Heksadesimal : ";
var_dump(0x1A); // Heksadesimal

echo "Biner : ";
var_dump(0b111111); // Biner

echo "Under Score di Number : ";
var_dump(1_234_567); // Under Score di Number

echo "Float : ";
var_dump(1.234); // Float

echo "Float dengan E notation Plus : ";
var_dump(1.2e3); // Float dengan E notation Plus (1.2 x 1000)

echo "Float dengan E notation Minus : ";
var_dump(1.2e-3); // Float dengan E notation Minus (7 x 0.001)

echo "Underscore di Float : ";
var_dump(1_234.567); // Underscore di Float

echo "Integer Overflow : ";
var_dump(9223372036854775807); // Integer Overflow


//type data number adalah tipe data yang digunakan untuk menyimpan data berupa angka baik itu desimal, oktal, heksadesimal, biner, float, dan lainnya